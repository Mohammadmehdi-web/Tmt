                                                                in changing parents&#39; attitudes towards education.
                                                                However, through the persistent efforts of TMT members
                                                                and support from the community, attitudes shifted, and
                                                                the noble
                                                                vision of the trust gained widespread acceptance.
                                                                A decade later, TMT established the Unity College Second
                                                                Shift, starting with nine students and aimed at
                                                                providing free
                                                                education to underprivileged children. Today, 40 years
                                                                later, both Unity College and Unity College Second Shift
