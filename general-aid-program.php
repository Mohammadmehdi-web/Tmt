                                                            <h5 class="mb-4 mt-5"><i class="fas fa-angle-right text-danger me-2"></i> Water Aid Program</h5>
                                                                <p class="ml-4 mt-0">The destitute and underprivileged often live in areas with poor infrastructure and limited access to clean water. 
                                                                This may lead to many health problems like diarrhea, Cholera and typhoid. These diseases can become fatal, especially for children. Everyone, 
                                                                irrespective of the financial status must have access clean and safe drinking water. TMT has taken initiative through this project to provide 
                                                                clean drinking water to as many as possible. The initiatives are taken in  </p>
                                                                <p class="ml-4 mt-0"><span class="fw-bold">•	Clean drinking water facilities at Schools: </span> Schools lacking safe drinking water were identified, and 14 water cooling machines with RO including submersible &nbsp;&nbsp;&nbsp;&nbsp;pumps have been installed to ensure children have access to safe water.</p>
                                                                <p class="ml-4 mt-0"><span class="fw-bold">•	Public water facilities to Communities in close vicinity where underprivileged families reside:  </span> Communities without safe, hygienic water were identified, and &nbsp;&nbsp;&nbsp;&nbsp;clean water facilities were provided, ensuring underprivileged families have round-the- clock access to clean water.</p>
                                                                <p class="ml-4 mt-0"><span class="fw-bold">•	Clean and Clod drinking water facilities for commuters on busy roads: </span> Clean and cold drinking water facilities were installed on busy roads, especially for the &nbsp;&nbsp;&nbsp;&nbsp;summer months. These facilities include water purifiers, cooling machines, and water outlets.</p>
                                                                <p class="ml-4 mt-0"><span class="fw-bold">•	Purified drinking water for homes of underprivileged families via water purifier: </span> TATA Swach units, a reputed water purifier brand in India, were distributed to &nbsp;&nbsp;&nbsp;&nbsp;400 families whose children are part of the Unity Education program, helping them lead healthier lives.</p>
                                                                <p class="ml-4 mt-0"><span class="fw-bold">•	Safe and Cold water at Hospitals: </span> Clean and cold drinking water facilities including 14 machines were installed in government hospitals, including KGMU, serving &nbsp;&nbsp;&nbsp;&nbsp;patients, attendants who cannot afford packaged drinking water.</p>

                                                                <h5 class="mb-4"><i class="fas fa-angle-right text-danger me-2"></i> Widow Aid Program</h5>
                                                                <p class="ml-4 mt-0">A society can flourish in a better way when widows, orphans and destitutes achieve freedom from poverty, can freely practice their rights, have a dignified life and realizing their potential thereby contributing towards their personal and family’s development which in turn will contribute towards development of society. This program endeavors to provide basic facilities to widows and their orphans, which otherwise are not accessible to them and results in hardships.</p> 
                                                                <p class="ml-4 mt-0">TMT tries in providing them with maximum social and financial independence through skill training and education so that they may not remain dependent on others. We put efforts to empower them and their children to become self-reliant. This assistance is provided till the time either the widow or their children become independent. We also provide monthly food aid and financial support to them for a specified time.</p>  

                                                                <h5 class="mb-4"><i class="fas fa-angle-right text-danger me-2"></i> Monthly Food Aid Program</h5>
                                                                <p class="ml-4 mt-0">TMT supports underprivileged families with uncooked food packets throughout the year or till the time it is required. The food aid is provided to the poor and needy without any discrimination on the lines of religion. </p>
                                                                <p class="ml-4 mt-0">The beneficiaries get registered with us and after proper survey and whether the need is genuine, the monthly food packets are being provided. The surveys are done by the field staff of TMT.</p>
                                                                <p class="ml-4 mt-0">The project serves families in Lucknow and other cities in Uttar Pradesh, including those with children enrolled in TMT's free education program (UEP). Additionally, food packets are regularly provided to the caretakers of pediatric cancer patients at King George Medical University (KGMU). All food packets are prepared hygienically by vendors under close supervision to ensure quality and safety.</p>

                                                                <h5 class="mb-4"><i class="fas fa-angle-right text-danger me-2"></i>Micro-Financing of Small Business</h5>
                                                                <p class="ml-4 mt-0">At TMT, we firmly believe that one of the best kinds of support is to enable a person to earn livelihood through small business ventures. Supporting a family for its monthly expenses, for a long duration is neither possible nor appreciable. TMT tries its best to encourage, motivate, guide and financially support willing individuals to start a small business or scale up existing businesses so that they earn a respectable living. The aspiring candidates are evaluated on their skills and business model. They are also trained in principles of operating business and account keeping so that they can keep track on returns on investment and operate their businesses successfully. </p>
                                                                <p class="ml-4 mt-0">Regular Monitoring is conducted to assess the health of business and in case of need, additional training and financial support is provided.</p>
                                                                

                                                                <h5 class="mb-4"><i class="fas fa-angle-right text-danger me-2"></i>Monthly Monetary Assistance</h5>
                                                                <p class="ml-4 mt-0">Regular financial aid to poor and needy individuals and families is provided, ensuring they can meet their basic daily needs 
                                                                and alleviate financial stress.
                                                                Proper enrollment is done of the Applicant and disbursement is done after verifying the Genuity of the cases through the field staff of TMT.
                                                                </p>
                                                               

                                                                <h5 class="mb-4"><i class="fas fa-angle-right text-danger me-2"></i>House Repair Assistance</h5>
                                                                <p class="ml-4 mt-0">Financial support for home repairs to ensure safe and habitable living conditions, improving the quality of life for underprivileged families.
                                                                The Applicant needs to enroll for the Assistance. Support is provided after survey of the property, financial status of the family, 
                                                                kind of repair through the field officers of TMT.
                                                                </p>


                                                                <h5 class="mb-4"><i class="fas fa-angle-right text-danger me-2"></i>Ramzan Food Aid Program</h5>
                                                                <p class="ml-4 mt-0">Each year in the month of Ramadan, we do a food drive by providing a monthly ration for a family of 4. 
                                                                The program also ensures the quality and quantity of the products delivered. More than 4500 food packets gets distributed to the under privileged families.</p>
                                                                

                                                                <h5 class="mb-4"><i class="fas fa-angle-right text-danger me-2"></i>General Welfare</h5>
